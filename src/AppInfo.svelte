<h1>Image Folder Resizer</h1>

<p>
	This tool is for batch resizing and reformating images within a folder-hierarchy.
	It is ideal for generating responsive images for websites where the original folder-structure has to be respected.
</p>

<p>
	This tool runs entirely in your browser using HTML canvases and the <a href="https://developer.mozilla.org/en-US/docs/Web/API/File_System_Access_API">File System Access API</a>. Your files are never sent to my servers.
</p>

<h2>Supported Formats</h2>
<ul>
	<li>PNG</li>
	<li>JPEG</li>
	<li>WebP</li>
</ul>

